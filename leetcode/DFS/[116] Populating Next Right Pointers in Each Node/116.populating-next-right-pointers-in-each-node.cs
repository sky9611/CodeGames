public class Solution
{
    public Node Connect(Node root)
    {
        Queue<Tuple<Node, int>> queue = new Queue<Tuple<Node, int>>();
        queue.Enqueue(new Tuple<Node, int>(root, 0));
        while (queue.Count > 0)
        {
            Tuple<Node, int> tuple = queue.Dequeue();
            Node node = tuple.Item1;
            int depth = tuple.Item2;
            if (node != null)
            {
                if(queue.Count>0)
                {
                    Node next = queue.Peek().Item1;
                    if (next != null && queue.Peek().Item2 == depth)
                        node.next = next;
                }
                queue.Enqueue(new Tuple<Node, int>(node.left, depth + 1));
                queue.Enqueue(new Tuple<Node, int>(node.right, depth + 1));
            }
            
        }
        return root;
    }
}

